/// Render `pg_hba.conf`. The instance has exactly one app database (`db_name`)
/// and one app user (`app_user`). The docker port is bound to 0.0.0.0 on
/// the host, so connections can arrive from any LAN address — auth is
/// scram-sha-256 with the password from state.toml. The `pgbackrest` role
/// is created via init SQL on first boot; it uses local-socket trust
/// because the container is the trust boundary. The `pgreplica` role
/// (non-SUPERUSER) is the only role allowed to replicate over TCP —
/// `pgforge clone` uses it for pg_basebackup.
pub fn generate_pg_hba(db_name: &str, app_user: &str) -> String {
    format!(
        r#"# Generated by pgforge — do not edit by hand.
# TYPE  DATABASE        USER            ADDRESS                 METHOD

# Local unix socket — superuser app user trusted for admin tasks via docker exec.
local   all             {user}                                   trust

# pgBackRest runs as postgres OS user inside this container; trust over the
# local socket avoids needing a .pgpass file for archive-push.
local   replication     pgbackrest                              trust
local   all             pgbackrest                              trust

# Clone via pg_basebackup from a sibling container over the docker bridge
# network. The replication role is non-SUPERUSER (see init_sql.rs) so even
# if its password leaks the blast radius is limited to streaming WAL.
host    replication     pgreplica       samenet                 scram-sha-256

# App user — reachable from any address (host docker binding is 0.0.0.0),
# scram-sha-256 password required. Combined with hosting on a single
# LAN behind a NAT, this is the same posture as RDS in a private VPC.
host    {db}             {user}            all                     scram-sha-256

# Default-deny everything else.
host    all             all             all                     reject
"#,
        user = app_user,
        db = db_name,
    )
}
